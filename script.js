// ========== VIDEO CONTROLS (FIXED) ==========
let currentVideo = null;
let isVideoControlsSetup = false;

function setupVideoControls(video) {
  if (!video || isVideoControlsSetup) return;
  
  const playPauseBtn = document.getElementById('playPauseBtn');
  const progressFill = document.getElementById('progressFill');
  const videoProgress = document.getElementById('videoProgress');
  const videoTime = document.getElementById('videoTime');
  const muteBtn = document.getElementById('muteBtn');
  const volumeSlider = document.getElementById('volumeSlider');
  const fullscreenBtn = document.getElementById('fullscreenBtn');
  
  if (!playPauseBtn || !videoProgress) return;
  
  function updatePlayPauseButton() {
    const playIcon = playPauseBtn?.querySelector('.play-icon');
    const pauseIcon = playPauseBtn?.querySelector('.pause-icon');
    if (video.paused) {
      if (playIcon) playIcon.style.display = 'block';
      if (pauseIcon) pauseIcon.style.display = 'none';
    } else {
      if (playIcon) playIcon.style.display = 'none';
      if (pauseIcon) pauseIcon.style.display = 'block';
    }
  }
  
  function togglePlayPause() {
    if (video.paused) {
      video.play().catch(e => console.log('Play error:', e));
    } else {
      video.pause();
    }
    updatePlayPauseButton();
  }
  
  playPauseBtn.removeEventListener('click', togglePlayPause);
  playPauseBtn.addEventListener('click', togglePlayPause);
  
  video.removeEventListener('play', updatePlayPauseButton);
  video.removeEventListener('pause', updatePlayPauseButton);
  video.addEventListener('play', updatePlayPauseButton);
  video.addEventListener('pause', updatePlayPauseButton);
  
  function updateProgress() {
    if (video.duration && !isNaN(video.duration)) {
      const percent = (video.currentTime / video.duration) * 100;
      if (progressFill) progressFill.style.width = percent + '%';
      
      const currentMin = Math.floor(video.currentTime / 60);
      const currentSec = Math.floor(video.currentTime % 60);
      const totalMin = Math.floor(video.duration / 60);
      const totalSec = Math.floor(video.duration % 60);
      if (videoTime) {
        videoTime.textContent = `${currentMin}:${currentSec.toString().padStart(2,'0')} / ${totalMin}:${totalSec.toString().padStart(2,'0')}`;
      }
    }
  }
  
  video.removeEventListener('timeupdate', updateProgress);
  video.addEventListener('timeupdate', updateProgress);
  
  function seekVideo(e) {
    const rect = videoProgress.getBoundingClientRect();
    const pos = (e.clientX - rect.left) / rect.width;
    if (video.duration && !isNaN(video.duration)) {
      video.currentTime = pos * video.duration;
    }
  }
  
  videoProgress.removeEventListener('click', seekVideo);
  videoProgress.addEventListener('click', seekVideo);
  
  if (volumeSlider) {
    function updateVolume() {
      video.volume = parseFloat(volumeSlider.value);
      video.muted = false;
    }
    volumeSlider.removeEventListener('input', updateVolume);
    volumeSlider.addEventListener('input', updateVolume);
    volumeSlider.value = video.volume || 1;
  }
  
  if (muteBtn) {
    function toggleMute() {
      if (video.muted) {
        video.muted = false;
        if (volumeSlider) volumeSlider.value = video.volume;
      } else {
        video.muted = true;
        if (volumeSlider) volumeSlider.value = 0;
      }
    }
    muteBtn.removeEventListener('click', toggleMute);
    muteBtn.addEventListener('click', toggleMute);
  }
  
  if (fullscreenBtn) {
    function toggleFullscreen() {
      const videoWrapper = document.querySelector('.video-wrapper');
      if (!videoWrapper) return;
      if (!document.fullscreenElement) {
        videoWrapper.requestFullscreen().catch(err => console.log(err));
      } else {
        document.exitFullscreen();
      }
    }
    fullscreenBtn.removeEventListener('click', toggleFullscreen);
    fullscreenBtn.addEventListener('click', toggleFullscreen);
  }
  
  video.play().catch(e => console.log('Auto-play prevented:', e));
  setTimeout(() => updatePlayPauseButton(), 100);
  
  isVideoControlsSetup = true;
}

function cleanupVideo() {
  if (currentVideo) {
    currentVideo.pause();
    currentVideo.currentTime = 0;
    currentVideo = null;
  }
  isVideoControlsSetup = false;
}

