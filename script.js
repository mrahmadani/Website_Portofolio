// ========== LOGIKA DRAG & ZOOM ==========
function initDrag(elementId, scaleObj, xObj, yObj, updateFn, resetBtnId, isGallery = false) {
  const container = document.getElementById(elementId);
  if (!container) return;
  let isDragging = false; let startX = 0; let startY = 0;

  const startDrag = (clientX, clientY) => { isDragging = true; startX = clientX - xObj.value; startY = clientY - yObj.value; };
  
  container.addEventListener('mousedown', (e) => { 
    if (e.target.closest('.floating-reset-btn') || e.target.closest('button') || e.target.closest('.modal-nav-btn')) return; 
    e.preventDefault(); startDrag(e.clientX, e.clientY); container.classList.add('cursor-grabbing'); 
  });
  window.addEventListener('mouseup', () => { isDragging = false; container.classList.remove('cursor-grabbing'); });
  window.addEventListener('mousemove', (e) => { if (!isDragging) return; xObj.value = e.clientX - startX; yObj.value = e.clientY - startY; updateFn(); });
  
  container.addEventListener('touchstart', (e) => { 
    if (e.target.closest('.floating-reset-btn') || e.target.closest('button') || e.target.closest('.modal-nav-btn')) return; 
    if (e.touches.length === 1) { startDrag(e.touches[0].clientX, e.touches[0].clientY); } 
  }, {passive: false});
  
  window.addEventListener('touchmove', (e) => { 
    if (isDragging && e.touches.length === 1) { 
      xObj.value = e.touches[0].clientX - startX; yObj.value = e.touches[0].clientY - startY; 
      updateFn(); 
    } 
  }, {passive: false});
  window.addEventListener('touchend', () => { isDragging = false; });
  
  container.addEventListener('wheel', (e) => { 
    e.preventDefault(); scaleObj.value = Math.min(Math.max(scaleObj.value + (e.deltaY < 0 ? 0.1 : -0.1), 0.5), 4); updateFn(); 
  }, {passive: false});
}

function initPinchZoom(elementId, scaleObj, xObj, yObj, updateFn) {
  const container = document.getElementById(elementId);
  if (!container) return;
  let initialDistance = 0, initialScale = 1, initialX = 0, initialY = 0;
  const getDistance = (touches) => Math.hypot(touches[0].clientX - touches[1].clientX, touches[0].clientY - touches[1].clientY);
  const getCenter = (touches) => ({ x: (touches[0].clientX + touches[1].clientX) / 2, y: (touches[0].clientY + touches[1].clientY) / 2 });
  
  container.addEventListener('touchstart', (e) => {
    if (e.touches.length === 2) {
      e.preventDefault(); initialDistance = getDistance(e.touches); initialScale = scaleObj.value;
      const center = getCenter(e.touches); initialX = center.x; initialY = center.y;
    }
  }, {passive: false});
  
  container.addEventListener('touchmove', (e) => {
    if (e.touches.length === 2) {
      e.preventDefault(); const newDistance = getDistance(e.touches);
      let newScale = initialScale * (newDistance / initialDistance);
      newScale = Math.min(Math.max(newScale, 0.5), 4); scaleObj.value = newScale;
      const newCenter = getCenter(e.touches);
      xObj.value += newCenter.x - initialX; yObj.value += newCenter.y - initialY;
      initialX = newCenter.x; initialY = newCenter.y; initialScale = scaleObj.value; updateFn();
    }
  }, {passive: false});
}

