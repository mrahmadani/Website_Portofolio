function updateNavButtonsVisibility() {
  const leftBtn = document.getElementById('modalNavLeft');
  const rightBtn = document.getElementById('modalNavRight');
  if (!leftBtn || !rightBtn) return;
  const isZoomedOrMoved = (modalScaleObj.value > 1.01 || modalScaleObj.value < 0.99) || (Math.abs(modalXObj.value) > 5) || (Math.abs(modalYObj.value) > 5);
  const hasMultiple = currentGallery.length > 1;
  
  if (!isZoomedOrMoved && hasMultiple) {
    leftBtn.classList.add('visible'); rightBtn.classList.add('visible');
    leftBtn.style.opacity = currentDocIndex === 0 ? '0.2' : '1';
    rightBtn.style.opacity = currentDocIndex === currentGallery.length-1 ? '0.2' : '1';
    leftBtn.style.pointerEvents = currentDocIndex === 0 ? 'none' : 'auto';
    rightBtn.style.pointerEvents = currentDocIndex === currentGallery.length-1 ? 'none' : 'auto';
  } else {
    leftBtn.classList.remove('visible'); rightBtn.classList.remove('visible');
  }
}

